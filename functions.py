print(word4.rstrip())# אחרי המילה
print(word4.strip())# מוריד רווחים לפני ואחרי

#החלפת תו מסויים במחרוזת בתו אחר
print(word4.replace('e','\\'))

#המתודה dir תחזיר רשימה שבה כל שמות המתודות שקיימות בפייתון עבור המשתנה שהתקבל, במקרה שלמטה עבור מחרוזת
print(dir(word4))




